# --- Example 1: Comments ---
# This line is a comment. Python ignores it completely.
# Comments help explain your code to humans.

# --- Example 2: Inline comments ---
print("hello")  # This prints the word hello
print(100)      # This prints the number 100

# --- Example 3: Statements (they DO something) ---
print("This is a statement")
x = 10  # This is a statement - it stores 10 in x
print(x)

# --- Example 4: Expressions (they PRODUCE a value) ---
# When used inside print(), you can see the result
print(2 + 3)    # expression: produces 5
print(10 * 2)   # expression: produces 20
print(100 - 1)  # expression: produces 99

# --- Example 5: Statement containing an expression ---
result = 5 + 3  # statement (assignment) containing expression (5 + 3)
print(result)

# --- Example 6: Correct indentation ---
# All code starts at the left edge (no extra spaces)
print("This is correct")
print("This is also correct")

# --- Example 7: Using comments to organize code ---
# ---- Section: Greetings ----
print("Welcome!")
print("Good to see you!")

# ---- Section: Numbers ----
print(42)
print(7 + 8)

